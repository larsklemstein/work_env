# ----------------------------------------------------------------------------
# declarations
# ----------------------------------------------------------------------------

readonly PROG=${0##*/}


# ----------------------------------------------------------------------------
# functions
# ----------------------------------------------------------------------------

msg_ok() {
	echo -e "\e[92m[$PROG] $1\e[39m" >&2
}

msg_warn() {
	echo -e "\e[93m[$PROG] $1\e[39m" >&2
}

msg_error() {
	echo -e "\e[91m[$PROG] $1\e[39m" >&2
}

msg_abort() {
	msg_error "$1"
	exit 1
}


# ----------------------------------------------------------------------------
# main
# ----------------------------------------------------------------------------

msg_warn "Ups..."

msg_ok "Started..."
